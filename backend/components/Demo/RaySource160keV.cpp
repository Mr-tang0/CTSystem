BOOL CRaySource160keVComm::IsValidFrame(const std::vector<BYTE>& frame)
{
    // ?????ßµ?Ôì??ß≥????????1+????2+???2+ßµ??1+?¶¬1=7????
    if (frame.size() < 7)
        return FALSE;

    // ??ßµ??
    if (frame[0] != STX)
        return FALSE;

    // ?¶¬ßµ??
    if (frame[frame.size() - 1] != ETX)
        return FALSE;

    // ßµ????ßµ?˜œßµ?˙î¶∂????????+?????+????+???????
    BYTE receivedCheckSum = frame[frame.size() - 2];
    std::vector<BYTE> checkData(frame.begin() + 1, frame.end() - 2); // ????????ßµ?????¶¬
    BYTE calculatedCheckSum = CalculateCheckSum(checkData);
    if (receivedCheckSum != calculatedCheckSum)
        return FALSE;

    return TRUE;
}
